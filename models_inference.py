"""## LLM As A Judge (Gemini 3.1 Flash-Lite, Gemini 3 Flash Preview, Gemini 2.5 Pro etc.)"""

import os
import json
import time
from google import genai


API_KEY = ""
INPUT_FILE = "/content/drive/MyDrive/llm_outputs/answers_Qwen_Qwen3.5-4B.json"
OUTPUT_FILE ="/content/drive/MyDrive/llm_judged/" + "judged_" + "Qwen_Qwen3.5-4B.json"


client = genai.Client(api_key=API_KEY)

def evaluate_with_judge(question, ground_truth, predicted_answer):
    prompt = f"""
    Ты — беспристрастный судья. Твоя задача — оценить ответ языковой модели на вопрос по научной статье.

    ВОПРОС: {question}
    ЭТАЛОННЫЙ ОТВЕТ: {ground_truth}
    ОТВЕТ МОДЕЛИ: {predicted_answer}

    Сравни ответ модели с эталонным. Оцени по шкале от 1 до 5:
    1 - Полный бред, галлюцинация или отказ отвечать.
    2 - Много ошибок, упущен главный смысл.
    3 - Частично верно, есть правильные факты, но суть не раскрыта полностью.
    4 - Правильно, но есть мелкие неточности или ответ слишком расплывчатый.
    5 - Идеально (смысл передан точно, даже если другими словами).

    Верни строго JSON:
    {{
        "score": 4,
        "reason": "краткое обоснование"
    }}
    """

    while True:
        try:
            response = client.models.generate_content(
            model='gemini-3.1-flash-lite-preview',
            contents={'text': prompt},
            config={
              'temperature': 0,
              'top_p': 0.95,
              'top_k': 20,
              },
            )
            clean_text = response.text.replace("```json", "").replace("```", "").replace("```JSON", "").strip()

            start_idx = clean_text.find('{')
            end_idx = clean_text.rfind('}')
            if start_idx != -1 and end_idx != -1:
                clean_text = clean_text[start_idx : end_idx + 1]

            return json.loads(clean_text)

        except json.JSONDecodeError:
            print("Ошибка парсинга JSON от судьи. Пробуем еще раз...")
            time.sleep(60)
        except Exception as e:
            error_msg = str(e).lower()
            if "429" in error_msg or "quota" in error_msg:
                print("Лимит API (Rate Limit). Ждем 30 секунд...")
                time.sleep(30)
            elif "503" in error_msg or "overloaded" in error_msg:
                print("Сервер Gemini перегружен. Ждем 10 секунд...")
                time.sleep(10)
            else:
                print(f"Неизвестная ошибка API: {e}. Ждем 5 сек...")
                time.sleep(5)

def main():
    if not os.path.exists(INPUT_FILE):
        print(f"Файл {INPUT_FILE} не найден. Сначала запустите первый скрипт.")
        return

    with open(INPUT_FILE, 'r', encoding='utf-8') as f:
        generated_data = json.load(f)

    judged_data = []
    processed_keys = set()

    if os.path.exists(OUTPUT_FILE):
        with open(OUTPUT_FILE, 'r', encoding='utf-8') as f:
            try:
                judged_data = json.load(f)
                processed_keys = {f"{r['article_id']}_{r['q_index']}" for r in judged_data}
                print(f"Восстановлено оценок: {len(judged_data)}")
            except json.JSONDecodeError:
                print("Файл оценок поврежден.")

    print("\n--- СТАРТ ОЦЕНКИ (Gemini API) ---")
    for item in generated_data:
        unique_key = f"{item['article_id']}_{item['q_index']}"

        if unique_key in processed_keys:
            continue

        print(f"[{unique_key}] Оценка ответа...")

        if item['predicted_answer'] in ["", "str", "assistant", "user", "text"]:
          item['predicted_answer'] = 'No answer'

        eval_result = evaluate_with_judge(
            item['question'],
            item['ground_truth'],
            item['predicted_answer']
        )

        item['judge_score'] = eval_result.get('score', 0)
        item['judge_reason'] = eval_result.get('reason', 'N/A')

        judged_data.append(item)
        print(f"Оценка: {item['judge_score']}/5 | Причина: {item['judge_reason'][:50]}...")

        with open(OUTPUT_FILE, 'w', encoding='utf-8') as f:
            json.dump(judged_data, f, ensure_ascii=False, indent=2)

        time.sleep(4)

    scores = [r['judge_score'] for r in judged_data if isinstance(r['judge_score'], (int, float))]
    if scores:
        avg = sum(scores) / len(scores)
        print(f"\nВСЕ ОТВЕТЫ ОЦЕНЕНЫ! Средний балл модели: {avg:.2f} / 5.0")
    print(f"Файл с результатами: {OUTPUT_FILE}")

if __name__ == "__main__":
    main()
