import os
import json
import time
import glob
from google import genai


API_KEY_GOOGLE = ""

INPUT_DIR = '/content/drive/MyDrive/articles_dump/'
OUTPUT_FILE = '/content/drive/MyDrive/dataset_benchLLM_7.json'

client = genai.Client(api_key=API_KEY_GOOGLE)

def generate_questions(text, topic):
    prompt = f"""
    Ты - эксперт по оценке знаний в области "{topic}".
    Твоя задача - прочитать научную статью и составить
    4 вопроса по её содержанию.

    Требования:
    1. Вопросы должны быть разной сложности.
    2. Вопросы должны решаться ИСКЛЮЧИТЕЛЬНО на основе текста статьи.
    Не используй внешние знания.
    3. Для каждого вопроса напиши эталонный ответ (ground_truth),
    который содержится в тексте.

    Верни результат СТРОГО в формате JSON:
    [
      {{
        "question": "Текст вопроса 1",
        "answer": "Эталонный ответ 1",
        "type": "fact"
      }},
      {{
        "question": "Текст вопроса 2",
        "answer": "Эталонный ответ 2",
        "type": "analysis"
      }}
      ... (всего 4 вопроса)
    ]

    ТЕКСТ СТАТЬИ:
    {text}
    """

    try:
        response = client.models.generate_content(
        model='gemini-3-flash-preview',
        contents={'text': prompt},
        config={
          'temperature': 0,
          'top_p': 0.95,
          'top_k': 20,
          },
        )

        raw_text = response.text

        clean_text = raw_text.replace("```json", "").replace("```", "").strip()
        clean_text = clean_text.replace("```JSON", "")

        start_index = clean_text.find('[')
        end_index = clean_text.rfind(']')

        if start_index != -1 and end_index != -1:
            clean_text = clean_text[start_index : end_index + 1]

        return json.loads(clean_text)

    except json.JSONDecodeError:
        print(f"Ошибка парсинга JSON. Получен текст:\n{raw_text[:200]}...")
        return None
    except Exception as e:
        print(f"Ошибка API: {e}")
        return None


def main():
    dataset = []

    md_files = glob.glob(f"{INPUT_DIR}/**/*.md", recursive=True)
    md_files = md_files

    print(f"Найдено файлов: {len(md_files)}")

    for i, file_path in enumerate(md_files):

        topic = os.path.basename(os.path.dirname(file_path))
        filename = os.path.basename(file_path)

        print(f"[{i+1}/{len(md_files)}]" + \
              "Обработка: {filename} ({topic})...", end=" ")

        try:
            with open(file_path, 'r', encoding='utf-8') as f:
                text = f.read()

            if len(text) < 500:
                print("SKIPPED (Too short)")
                continue

            qa_pairs = generate_questions(text, topic)

            if qa_pairs:
                entry = {
                    "id": f"{topic}_{i}",
                    "topic": topic,
                    "filename": filename,
                    "context": text,
                    "qas": qa_pairs
                }
                dataset.append(entry)
                print("OK")
            else:
                print("FAIL (No output)")

            time.sleep(4)

            if (i+1) % 10 == 0:
                with open(OUTPUT_FILE, 'w', encoding='utf-8') as f:
                    json.dump(dataset, f, ensure_ascii=False, indent=2)
                    print(f"Промежуточное сохранение ({len(dataset)} записей)")

        except Exception as e:
            print(f"ERROR: {e}")

    with open(OUTPUT_FILE, 'w', encoding='utf-8') as f:
        json.dump(dataset, f, ensure_ascii=False, indent=2)

    print(f"\nДатасет сохранен в {OUTPUT_FILE}")


if __name__ == "__main__":
    main()