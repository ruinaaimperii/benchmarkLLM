def get_robust_session():
    session = requests.Session()
    retry = Retry(total=3, backoff_factor=2, status_forcelist=[500, 502, 503, 504])
    adapter = HTTPAdapter(max_retries=retry)
    session.mount('http://', adapter)
    session.mount('https://', adapter)
    session.headers.update({
        'User-Agent': 'Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/120.0.0.0 Safari/537.36'
    })
    return session


def get_random_date(start_year, end_year):
    start_date = date(start_year, 1, 1)
    end_date = date(end_year, 12, 31)
    days_between = (end_date - start_date).days
    random_days = random.randrange(days_between)
    return (start_date + timedelta(days=random_days)).isoformat()


def ensure_dir(directory):
    if not os.path.exists(directory):
        os.makedirs(directory)


def count_existing_files():
    counts = {}
    ensure_dir(OUTPUT_DIR)
    for topic in TARGET_TOPICS:
        path = os.path.join(OUTPUT_DIR, topic)
        ensure_dir(path)

        files = [f for f in os.listdir(path) if f.endswith('.pdf')]
        counts[topic] = len(files)
    return counts


def is_russian(metadata):
    title = metadata.get('title', [''])[0]
    if re.search('[а-яА-Я]', title):
        return True

    return False


def get_keywords_strict(url, session):
    try:
        response = session.get(url, timeout=10)
        if response.status_code != 200: return None
        soup = BeautifulSoup(response.text, 'lxml')
        meta_keywords = soup.find('meta', attrs={'name': 'keywords'})
        if meta_keywords and meta_keywords.get('content'):
            content = meta_keywords['content'].lower()
            if len(content) < 5: return None
            return content
        return None
    except Exception:
        return None


def download_pdf(url, filepath, session):
    try:
        pdf_url = url if url.endswith('.pdf') else url + ".pdf"
        response = session.get(pdf_url, stream=True, timeout=30)
        if response.status_code == 200:
            with open(filepath, 'wb') as f:
                for chunk in response.iter_content(1024):
                    f.write(chunk)
            return True
        return False
    except Exception:
        return False


def check_topic(keywords_text, current_counts):
    if not keywords_text: return None
    for topic, keywords in TARGET_TOPICS.items():
        if current_counts[topic] >= LIMIT_PER_TOPIC:
            continue
        for keyword in keywords:
            if keyword in keywords_text:
                return topic
    return None

def main():
    session = get_robust_session()
    sickle = Sickle(OAI_URL)
    sickle.session = session

    counts = count_existing_files()
    print("Текущий прогресс в папках:")
    print(" | ".join([f"{k}:{v}" for k, v in counts.items()]))

    if all(c >= LIMIT_PER_TOPIC for c in counts.values()):
        print("Все папки уже заполнены")
        return

    random_from_date = get_random_date(START_YEAR, END_YEAR)
    print(f"\nСтарт с даты: {random_from_date}")

    try:
        records = sickle.ListRecords(metadataPrefix='oai_dc', **{'from': random_from_date})
    except Exception as e:
        print(f"Ошибка OAI: {e}")
        return

    scanned_count = 0

    for record in records:
        scanned_count += 1

        if all(c >= LIMIT_PER_TOPIC for c in counts.values()):
            print("\nЗАДАЧА ВЫПОЛНЕНА")
            break

        if not record.metadata: continue

        if not is_russian(record.metadata):
            continue

        identifiers = record.metadata.get('identifier', [])
        article_link = next((id for id in identifiers if id.startswith('http')), None)
        if not article_link: continue

        keywords_text = get_keywords_strict(article_link, session)

        found_topic = check_topic(keywords_text, counts)

        if found_topic:
            title = record.metadata.get('title', ['No Title'])[0]

            safe_title = "".join([c for c in title if c.isalpha() or c.isdigit() or c==' ']).strip()[:50]

            filename = f"{counts[found_topic]+1}_{safe_title}.pdf"
            filepath = os.path.join(OUTPUT_DIR, found_topic, filename)

            if os.path.exists(filepath):
                print(f"Файл уже существует, пропускаем: {filename}")
                continue

            print(f"\n[{found_topic.upper()}: {counts[found_topic]+1}/20] + {title[:40]}...")

            if download_pdf(article_link, filepath, session):
                counts[found_topic] += 1
                time.sleep(1)
        else:

            if scanned_count % 10 == 0:
                print(".", end="", flush=True)

        if scanned_count % 50 == 0:
            status = " | ".join([f"{k}:{v}" for k, v in counts.items()])
            print(f"\n--- Скан: {scanned_count}. Статус: [{status}] ---")

if __name__ == "__main__":
    main()

